package org.bonitasoft.studio.actors.ui.section.filter;

import org.bonitasoft.studio.model.process.Assignable;
import org.bonitasoft.studio.model.process.Lane;
import org.eclipse.emf.ecore.EObject;
import org.eclipse.gmf.runtime.diagram.ui.editparts.IGraphicalEditPart;
import org.eclipse.jface.viewers.IFilter;

/**
 * @author Romain Bioteau
 *
 */
public class AssignableActorsFilter implements IFilter {

	/* (non-Javadoc)
	 * @see org.eclipse.jface.viewers.IFilter#select(java.lang.Object)
	 */
	@Override
	public boolean select(Object element) {
		if (element instanceof IGraphicalEditPart) {
			EObject elem = ((IGraphicalEditPart)element).resolveSemanticElement() ;
			return  elem instanceof Assignable && !(elem instanceof Lane);
		}
		return false ;
	}

}
