package org.bonitasoft.studio.application.ui.control.model.dependency;

public class BonitaArtifactDependency {

    private String name;
    private String description;
    private String type;
    private String icon;
    private String groupId;
    private String artifactId;
    private String version;

    public BonitaArtifactDependency() {
    }

    public BonitaArtifactDependency(String name, String description, String type, String icon,
            String groupId, String artifactId, String version) {
        this.name = name;
        this.description = description;
        this.type = type;
        this.icon = icon;
        this.groupId = groupId;
        this.artifactId = artifactId;
        this.version = version;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public String getGroupId() {
        return groupId;
    }

    public void setGroupId(String groupId) {
        this.groupId = groupId;
    }

    public String getArtifactId() {
        return artifactId;
    }

    public void setArtifactId(String artifactId) {
        this.artifactId = artifactId;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }
}
