package org.bonitasoft.studio.properties.sections.forms.wizard.provider;

import java.util.Collection;

import org.bonitasoft.studio.diagram.form.custom.model.WidgetMapping;
import org.eclipse.core.runtime.Assert;
import org.eclipse.jface.viewers.ITreeContentProvider;
import org.eclipse.jface.viewers.Viewer;

/**
 * @author Romain Bioteau
 *
 */
public class WidgetMappingTreeContentProvider implements ITreeContentProvider {

	/* (non-Javadoc)
	 * @see org.eclipse.jface.viewers.IContentProvider#dispose()
	 */
	@Override
	public void dispose() {

	}

	/* (non-Javadoc)
	 * @see org.eclipse.jface.viewers.IContentProvider#inputChanged(org.eclipse.jface.viewers.Viewer, java.lang.Object, java.lang.Object)
	 */
	@Override
	public void inputChanged(Viewer viewer, Object oldInput, Object newInput) {

	}

	/* (non-Javadoc)
	 * @see org.eclipse.jface.viewers.ITreeContentProvider#getElements(java.lang.Object)
	 */
	@Override
	public Object[] getElements(Object inputElement) {
		String message = "input must be a instance of "+Collection.class.getName()+"<"+WidgetMapping.class.getName()+">";
		Assert.isLegal(inputElement instanceof Collection, message);
		Collection<?> collection = Collection.class.cast(inputElement);
		if(!collection.isEmpty()){
			Assert.isLegal(collection.iterator().next() instanceof WidgetMapping ,message);
		}
		return collection.toArray();
	}

	/* (non-Javadoc)
	 * @see org.eclipse.jface.viewers.ITreeContentProvider#getChildren(java.lang.Object)
	 */
	@Override
	public Object[] getChildren(Object parentElement) {
		if(parentElement instanceof WidgetMapping){
			WidgetMapping mapping = (WidgetMapping) parentElement;
			return mapping.getChildren().toArray();
		}
		return null;
	}

	/* (non-Javadoc)
	 * @see org.eclipse.jface.viewers.ITreeContentProvider#getParent(java.lang.Object)
	 */
	@Override
	public Object getParent(Object element) {
		return null;
	}

	/* (non-Javadoc)
	 * @see org.eclipse.jface.viewers.ITreeContentProvider#hasChildren(java.lang.Object)
	 */
	@Override
	public boolean hasChildren(Object element) {
		if(element instanceof WidgetMapping){
			WidgetMapping mapping = (WidgetMapping) element;
			return !mapping.getChildren().isEmpty();
		}
		return false;

	}

}
